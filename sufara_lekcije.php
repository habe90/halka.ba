                           <p>Kao što smo i rekli Elif izgleda kao vertikalna crta na koju i ispod koje dolaze vokali te se tada izgovara samo vokal koji je na Elifu.U određenim slučajevima izgovara se i Elif ( na početku pojedinih sura koje počinju sa kraticama, pojedinačnim harfovima ) o čemu ćemo govoriti u nekoj od sljedećih lekcija.<br> <br> 
                    
                            Harf WAW svojim izgledom podsjeca na naš broj 9, izgovara se tako da se namjeste usne za izgovor slova “O” pa se izgovori “W” tako da usne pritom formiraju kružni oblik. Izgovara se <b>kao W u engleskoj riječi World</b> te zato koristimo englesku transkripciju ovog harfa, a nikako kao bosansko “V”.<br> <br> 
                            
                            Ovaj harf se takoder <b>veže samo sa svoje desne strane</b>, kada na njemu dođe neki od vokala, prvo izgovorimo “W” pa onda vokal (npr. we, wi, wu), a kada je na njemu sukun tada se izgovara samo “W”.</p>
